    private void savebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savebtnActionPerformed
        try{
           Class.forName("com.mysql.jdbc.Driver");
           Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/veterinar_database","root","deki");
           String query = "insert into klijenti_table(ime_ljubimca, ime_vlasnika, pol, tegobe, rasa)values(?,?,?,?,?)";
           PreparedStatement pst = conn.prepareStatement(query);
           pst.setString(1, name.getText());
           pst.setString(2, ownerName.getText());
           if(male.isSelected()){
               gender = "Muski";
           }
           if(female.isSelected()){
               gender = "Zenski";
           }
           pst.setString(3, gender);
           String illness = "";
           if(parasites.isSelected()){
               illness += parasites.getText() + " ";
           }
            if(injury.isSelected()){
               illness += injury.getText() + " ";
           }
            pst.setString(4, illness);
            String race;
            race = species.getSelectedItem().toString();
            pst.setString(5, race);
            pst.executeUpdate();
            DefaultTableModel model = (DefaultTableModel)jTable1_show_users.getModel();
            model.setRowCount(0);
            show_user();
            JOptionPane.showMessageDialog(null, "Ljubimac uspesno dodat u bazu podataka");
        }
        catch(Exception e){
           JOptionPane.showMessageDialog(null,e);
       }
    }//GEN-LAST:event_savebtnActionPerformed

