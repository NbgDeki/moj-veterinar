    private void jTable1_show_usersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1_show_usersMouseClicked
        int i = jTable1_show_users.getSelectedRow();
        TableModel model = jTable1_show_users.getModel();
        name.setText(model.getValueAt(i, 1).toString());
        ownerName.setText(model.getValueAt(i, 2).toString());
        String catGender = model.getValueAt(i, 3).toString();
            if(catGender.equals("Muski")){
                male.setSelected(true);
            }else{
                female.setSelected(true);
            }
        String catTegobe = model.getValueAt(i, 4).toString();
            switch(catTegobe){
                case "Paraziti ":
                   parasites.setSelected(true);
                   injury.setSelected(false);
                   break;
            case "Povreda ":
                   parasites.setSelected(false);
                   injury.setSelected(true);
                   break; 
            default:
                   parasites.setSelected(true);
                   injury.setSelected(true);
            }
        String catRace = model.getValueAt(i, 5).toString();
            switch(catRace){
                case "Sijamska macka":
                    species.setSelectedIndex(0);
                    break;
                case "Bengalska macka":
                    species.setSelectedIndex(1);
                    break;
                case "Havana Braon":
                    species.setSelectedIndex(2);
                    break;
                case "Domaca macka":
                    species.setSelectedIndex(3);
            }
    }//GEN-LAST:event_jTable1_show_usersMouseClicked

