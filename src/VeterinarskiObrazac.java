    public ArrayList<User> userList(){
        ArrayList<User> usersList = new ArrayList<>();
        try{
           Class.forName("com.mysql.jdbc.Driver");
           Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/veterinar_database","root","deki");
           String query1 = "SELECT * FROM klijenti_table";
           Statement st = conn.createStatement();
           ResultSet rs = st.executeQuery(query1);
           User user;
           while(rs.next()){
               user = new User(rs.getInt("id"), rs.getString("ime_ljubimca"), rs.getString("ime_vlasnika"), rs.getString("pol"), rs.getString("tegobe"), rs.getString("rasa"));
               usersList.add(user);
           }
        }
        catch(Exception e){
           JOptionPane.showMessageDialog(null,e);
        }
        return usersList;
    }
    public void show_user(){
        ArrayList<User> list = userList();
        DefaultTableModel model = (DefaultTableModel)jTable1_show_users.getModel();
        Object[] row = new Object[6];
        for(int i=0; i<list.size();i++){
            row[0] = list.get(i).getId();
            row[1] = list.get(i).getImeLjubimca();
            row[2] = list.get(i).getImeVlasnika();
            row[3] = list.get(i).getPol();
            row[4] = list.get(i).getTegobe();
            row[5] = list.get(i).getRasa();
            model.addRow(row);
        }
    }
